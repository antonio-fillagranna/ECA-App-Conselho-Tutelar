                    <h1>Lei nº 8.069, de 13 de julho de 1990.</h1>
                    <h2>Dispõe sobre o Estatuto da Criança e do Adolescente e dá outras providências.</h2>

                    <p>O PRESIDENTE DA REPÚBLICA, faço saber que o Congresso Nacional decreta e eu sanciono a seguinte Lei:</p>

                    <h3>TÍTULO I</h3>
                    <h4>Das Disposições Preliminares</h4>

                    <p>Art. 1º Esta Lei dispõe sobre a proteção integral à criança e ao adolescente.</p>

                    <p>Art. 2º Considera-se criança, para os efeitos desta Lei, a pessoa até doze anos de idade incompletos, e adolescente aquela entre doze e dezoito anos de idade.</p>

                    <p>Parágrafo único. Nos casos expressos em lei, aplica-se excepcionalmente este Estatuto às pessoas entre dezoito e vinte e um anos de idade.</p>


                    <p>Art. 4º É dever da família, da comunidade, da sociedade em geral e do poder público assegurar, com absoluta prioridade, a efetivação dos direitos referentes à vida, à saúde, à alimentação, à educação, ao esporte, ao lazer, à profissionalização, à cultura, à dignidade, ao respeito, à liberdade e à convivência familiar e comunitária.</p>
                    <p>Parágrafo único. A garantia de prioridade compreende:<br>

                        a) primazia de receber proteção e socorro em quaisquer circunstâncias;<br>

                        b) precedência de atendimento nos serviços públicos ou de relevância pública;<br>

                        c) preferência na formulação e na execução das políticas sociais públicas;<br>

                        d) destinação privilegiada de recursos públicos nas áreas relacionadas com a proteção à infância e à juventude.</p>
                    <p>Art. 5º Nenhuma criança ou adolescente será objeto de qualquer forma de negligência, discriminação, exploração, violência, crueldade e opressão, punido na forma da lei qualquer atentado, por ação ou omissão, aos seus direitos fundamentais.</p>
                    <p>Art. 6º Na interpretação desta Lei levar-se-ão em conta os fins sociais a que ela se dirige, as exigências do bem comum, os direitos e deveres individuais e coletivos, e a condição peculiar da criança e do adolescente como pessoas em desenvolvimento.</p>
                    <h3>TÍTULO II</h3>
                    <h4>Dos Direitos Fundamentais</h4>
                    <h4>Capítulo I</h4>
                    <h5>Do Direito à Vida e à Saúde</h5>
                    <p>Art. 7º A criança e o adolescente têm direito a proteção à vida e à saúde, mediante a efetivação de políticas sociais públicas que permitam o nascimento e o desenvolvimento sadio e harmonioso, em condições dignas de existência.</p>
                    <p>Art. 8º É assegurado a todas as mulheres o acesso aos programas e às políticas de saúde da mulher e de planejamento reprodutivo e, às gestantes, nutrição adequada, atenção humanizada à gravidez, ao parto e ao puerpério e atendimento pré-natal, perinatal e pós-natal integral no âmbito do Sistema Único de Saúde. (Redação dada pela Lei nº 13.257, de 2016)</p>
                    <p>§ 1º O atendimento pré-natal será realizado por profissionais da atenção primária. (Redação dada pela Lei nº 13.257, de 2016)</p>
                    <p>§ 2º Os profissionais de saúde de referência da gestante garantirão sua vinculação, no último trimestre da gestação, ao estabelecimento em que será realizado o parto, garantido o direito de opção da mulher. (Redação dada pela Lei nº 13.257, de 2016)</p>

